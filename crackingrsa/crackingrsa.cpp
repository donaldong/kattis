int get_phi(int n) {
    if (n % 2 == 0) return n / 2 - 1;
    for (int i = 3; i * i < n; i += 2) {
        if (n % i == 0) return (i - 1) * (n / i - 1);
    }
    return -1;
}

ll gcdExt(ll a, ll b, ll &x, ll &y) {
    if (a == 0) {
        x = 0;
        y = 1;
        return b;
    }
    ll x1, y1;
    ll gcd = gcdExt(b % a, a, x1, y1);
    x = y1 - (b/a) * x1;
    y = x1;
    return gcd;
}

ll solve(ll e, ll phi) {
    ll d, k;
    ll gcd = gcdExt(e, phi, d, k);
    d /= gcd;
    if (d < 0) {
        ll n = -d / phi;
        if (-d % phi) ++n;
        d += n * phi;
    }
    if (d >= phi) {
        ll n = d / phi;
        d -= n * phi;
    }
    return d;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(0);
    int T;
    cin >> T;
    while (T--) {
        int n, e;
        cin >> n >> e;
        int phi = get_phi(n);
        ll d = solve(e, phi);
        cout << d << endl;
    }
    return 0;
}

inline void scan(int &number) {
    bool negative = false;
    int c;
    number = 0;
    c = getchar();
    if (c=='-') {
        negative = true;
        c = getchar();
    }
    for (; (c>47 && c<58); c=getchar()) number = number *10 + c - 48;
    if (negative) number *= -1;
}

