int get_multiple(int n, int target) {
    for (int i = 1; i <= 9; ++i) {
        if ((i * n + target) % 10 == 1) return i;
    }
    return -1;
}

int solve(int n) {
    int k = get_multiple(n % 10, 0);
    int p = n * k;
    int count = 0;
    if (p % 10 == 1) {
        p /= 10;
        ++count;
    }
    while (p) {
        while (p % 10 == 1) {
            ++count;
            p /= 10;
        }
        if (!p) return count;
        k = get_multiple(n % 10, p % 10);
        p += n * k;
    }
    return count;
}

