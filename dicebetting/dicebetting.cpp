vector<ld> nC(int n) {
    if (n == 0) return {1.0};
    vector<ld> cur = {1.0, 1.0};
    while (cur.size() <= n) {
        vector<ld> next(cur.size() + 1);
        next[0] = 1.0;
        for (int i = 1; i < cur.size(); ++i) {
            next[i] = cur[i - 1] + cur[i];
        }
        next.back() = 1.0;
        cur = next;
    }
    return cur;
}

