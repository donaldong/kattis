ull solve(string n) {
    if (n == "0") return 0;
    if (n == "1") return 1;
    char c = n[0];
    n.erase(0, 1);
    if (c == '1') {
        ull res = 1;
        res <<= n.size() + 1;
        return res - solve(n) - 1;
    }
    return solve(n);
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(0);
    int n;
    string a, b;
    cin >> n >> a >> b;
    ull ra = solve(a);
    ull rb = solve(b);
    if (ra > rb) cout << ra - rb - 1 << endl;
    else cout << rb - ra - 1 << endl;
    return 0;
}

inline void scan(int &number) {
    bool negative = false;
    int c;
    number = 0;
    c = getchar();
    if (c=='-') {
        negative = true;
        c = getchar();
    }
    for (; (c>47 && c<58); c=getchar()) number = number *10 + c - 48;
    if (negative) number *= -1;
}

