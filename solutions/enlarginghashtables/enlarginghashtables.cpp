hset<ull> np;
hset<ull> p;

bool P(ull n) {
    if (n == 2) return true;
    if (!(n & 1)) return false;
    if (p.find(n) != p.end()) return true;
    if (np.find(n) != np.end()) return false;
    for (ull i = 3; i * i <= n; i += 2) {
        if (n % i == 0) {
            np.insert(n);
            return false;
        }
    }
    p.insert(n);
    return true;
}

ull solve(ull n) {
    for (ull i = 2 * n + 1;; i += 2) {
        if (P(i)) return i;
    }
    return 0;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(0);
    while (true) {
        int n;
        scan(n);
        if (!n) break;
        ull res = solve(n);
        if (P(n)) cout << res << endl;
        else cout << res << " (" << n << " is not prime)" << endl;
    }
    return 0;
}

inline void scan(int &number) {
    bool negative = false;
    int c;
    number = 0;
    c = getchar();
    if (c=='-') {
        negative = true;
        c = getchar();
    }
    for (; (c>47 && c<58); c=getchar()) number = number *10 + c - 48;
    if (negative) number *= -1;
}

