int main() {
    ios::sync_with_stdio(false);
    cin.tie(0);
    int t;
    cin >> t;
    while (t--) {
        ull l, m;
        cin >> l >> m;
        queue<int> L, R;
        while (m--) {
            int n;
            string side;
            cin >> n >> side;
            if (side == "left") L.push(n);
            else R.push(n);
        }
        l *= 100;
        bool left = true;
        int count = 0;
        while (!L.empty() || !R.empty()) {
            ull cur = 0;
            if (left) {
                while (!L.empty()) {
                    if (cur + L.front() <= l) {
                        cur += L.front();
                        L.pop();
                    } else break;
                }
            } else {
                while (!R.empty()) {
                    if (cur + R.front() <= l) {
                        cur += R.front();
                        R.pop();
                    } else break;
                }
            }
            left = !left;
            ++count;
        }
        cout << count << endl;
    }
    return 0;
}

inline void scan(int &number) {
    bool negative = false;
    int c;
    number = 0;
    c = getchar();
    if (c=='-') {
        negative = true;
        c = getchar();
    }
    for (; (c>47 && c<58); c=getchar()) number = number *10 + c - 48;
    if (negative) number *= -1;
}

